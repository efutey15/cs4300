#Task 2: Implements integers, floating-point numbers, strings, and boolean

#Integer Implementation
def int_mult(int1, int2):
    intResult = int1*int2
    return intResult

#Floating Point Implementation
def float_add(num1, num2):
    floatResult = num1 + num2
    return floatResult

#String Implementation
def string_concat(str1, str2):
    concatination = str1 + str2
    return concatination

#Boolean Implementation
def boolean_truth(color):
    boolResult = bool(color)
    return boolResult